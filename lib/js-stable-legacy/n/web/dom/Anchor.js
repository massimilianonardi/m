//------------------------------------------------------------------------------
// Class -----------------------------------------------------------------------
//------------------------------------------------------------------------------

function Anchor(properties)
{
  return Class(Anchor).construct(this, undefined, properties, ["a"]);
}

Class(Anchor)
.inherit(Element)
.properties()
;

//------------------------------------------------------------------------------
// Methods ---------------------------------------------------------------------
//------------------------------------------------------------------------------

Anchor.prototype.href = function(value)
{
  if(arguments.length === 0)
  {
    return this.node.href;
  }
  else if(typeof value === "string")
  {
    this.node.href = value;
    
    return this;
  }
  else
  {
    throw new TypeError();
  }
};

