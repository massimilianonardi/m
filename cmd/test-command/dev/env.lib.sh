#!/bin/sh

#-------------------------------------------------------------------------------

env_list()
{
  if [ -z "$*" ]
  then
    set | sed 's|=.*||g'
  else
    set | grep -e "$@" | sed 's|=.*||g'
  fi
}

#-------------------------------------------------------------------------------

return_quoted_var()
{
  eval quoted="\$(quote \"\$$1\")"
  eval printf "$1=\"$quoted\" ."
}

#-------------------------------------------------------------------------------

env_return()
{
  for k in $@
  do
    return_quoted_var "$k"
  done
}

#-------------------------------------------------------------------------------

export_quoted_var()
{
  eval quoted="\$(quote \"\$$1\")"
  eval echo "export $1=\"$quoted;\""
}

#-------------------------------------------------------------------------------

env_export()
{
  for k in $@
  do
    export_quoted_var "$k"
  done
}

#-------------------------------------------------------------------------------
